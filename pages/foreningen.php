<blockquote>
    Våren 2010 hölls jubileumet UppCon:10 där allmänheten tilläts ta del av UppCon.
</blockquote>

<p class="large_text" style="margin-left: 35px">
    Den ideella föreningen <span class="large">Uppsalakai</span> bildades 2001 av <span class="large">Daniel Mauno Pettersson</span> och ett mindre gäng anime- och mangaentusiaster. 
    Föreningens första arrangemang, <span class="large">UppCon:01</span>, ägde rum 2002 och lockade ett <span class="large">femtiotal</span> intresserade. Åtta år senare, våren 2010, hölls jubileumet <span class="large">UppCon:10</span> där allmänheten fick fritt inträde till Vaksala Torg som hade hyrts utöver de vanliga inomhuslokalerna. UppCon:10 hade <span class="large">3000 besökare</span>, allmänheten icke inräknad.
</p>


