<p class="large_text clearfix">
    <a href="/foreningen">Uppsalakai</a> är en <a href="http://www.sverok.se">Sverok-ansluten</a> ideell förening med säte i Uppsala. Föreningens syfte är att främja intresse för östasien och östasiatisk populärkultur bland svenska ungdomar. <br />
    Främst bland föreningens aktiviteter är konventet <a href="http://www.uppcon.se">UppCon</a>, som är Nordens största arrangemang i sitt slag med cirka 4000 besökare.
</p>